'''
#Задание Даны строки S и S0. Удалить из строки S все подстроки, совпадающие с S0. 
Если совпадающих подстрок нет, то вывести строку S без изменений.
'''
try:
    S = input("Введите строку S: ")
    S0 = input("Введите строку S0: ")
  
    result = S.replace(S0, "")
    
    print(f"Исходная строка S: '{S}'")
    print(f"Строка для удаления S0: '{S0}'")
    print(f"Результат: '{result}'")

except Exception as e:
    print(f"Произошла ошибка: {e}")
