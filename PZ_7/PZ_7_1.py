'''
#Задание Даны целые положительные числа N1 и N2 и строки S1 и S2. 
Получить из этих строк новую строку, содержащую первые N1 символов строки S1 и последние N2 символов строки S2 (в указанном порядке).
'''
try:
    N1 = int(input("Введите N1 (целое положительное): "))
    N2 = int(input("Введите N2 (целое положительное): "))
    S1 = input("Введите строку S1: ")
    S2 = input("Введите строку S2: ")
    
    if N1 <= 0 or N2 <= 0:
        print("N1 и N2 должны быть положительными!")
    else:
        part1 = S1[:N1]
        
        
        part2 = S2[-N2:] if N2 <= len(S2) else S2
        
        
        result = part1 + part2
       
        print(f"Первые {N1} символов S1: '{part1}'")
        print(f"Последние {N2} символов S2: '{part2}'")
        print(f"Результат: '{result}'")

except ValueError:
    print("Ошибка: N1 и N2 должны быть целыми числами!")
